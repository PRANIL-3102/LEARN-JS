const marks = 82;

if (marks >= 90) {
    console.log("Excellent! You scored an A+.");
}
else if (marks >= 80) {
    console.log("Great job! You scored an A.");
}
else if (marks >= 70) {
    console.log("Good work! You scored a B.");
}
else if (marks >= 60) {
    console.log("You passed! You scored a C.");
}
else {
    console.log("You failed. Better luck next time!");
}